# have 0, 1, A: 000, 001, 010, 100, 110, 101, 011, 111
d = {0: 13, 1: 1, 10: 1, 100: 0, 11: 0, 101: 0, 110: 0, 111: 0}
s = 0
for i in range(16-1):
    newd = {0: 0, 1: 0, 10: 0, 100: 0, 11: 0, 101: 0, 110: 0, 111: 0}
    newd[0] = d[0]*13
    newd[1] = d[1]*14+d[0]
    newd[10] = d[10]*14+d[0]
    newd[100] = d[100]*14+d[0]
    newd[11] = d[11]*15+d[10]+d[1]
    newd[101] = d[101]*15+d[100]+d[1]
    newd[110] = d[110]*15+d[100]+d[10]
    newd[111] = d[111]*16+d[110]+d[101]+d[11]
    s += newd[111]
    d = newd
print(s)

